package Clases;

public class Habitat {

	//Atributos
		private int id;
		private String nombre;
	
	//Set&Get
		public int getId() {
			return id;
		}
		public void setId(int id) {
			this.id = id;
		}
		public String getNombre() {
			return nombre;
		}
		public void setNombre(String nombre) {
			this.nombre = nombre;
		}
	
}
